local N_mt = {
    __index = function(t,k)
        --[[
            '123'   [4] => 1
        ]]
        local m = k%#t
        if m==0 then
            return t[#t]
        else
            return t[m]
        end
    end
}


local paz= function(s)
    local t = {
        ins = table.insert
    }
    for i=1, s:len() do
        t:ins(s:sub(i,i))
    end
    return setmetatable(t, N_mt)
end


local main = {
    ins=table.insert
}

for str in string:gmatch("[^\n]+") do
    if str:len() ~= 0 then
        main:ins(paz(str))
    end
end

function solve(st_x, st_y)
    local trees=0

    local x = 1
    for y=1, #main, st_y do
        if main[y][x] == "#" then
            trees = trees + 1
        end
        x = x + st_x
    end

    return trees
end


local procs = {
    {1;1}; {3;1}; {5;1}; {7;1}; {1;2}
}

local total_trees = 1

for k,v in pairs(procs) do
    total_trees = total_trees * solve(v[1], v[2])
end

